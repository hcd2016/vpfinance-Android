	public static ArrayList<City> getCity(Province province) {
		if (province == null) {
			return null;
		}
		City allCity[] = City.values();
		ArrayList<City> citys = new ArrayList<City>();
		for(City c:allCity)
		{
			if(c.pid == province.id)
			{
				citys.add(c);
			}
		}
		
		Collections.sort(citys, new Comparator<ProvinceAndCity.City>() {
			@Override
			public int compare(City lhs, City rhs) {
				return lhs.order - rhs.order;
			}
		});
		
		return citys;
	}
	
